
public class Bitwise_Operator {

	public static void main(String[] args) {
		int a=12,b=25,c,d,e;
		c=a|b;
		System.out.println(c);
		d=a&b;
		System.out.println(d);
		e=a^b;
		System.out.println(e);
		int f=~b;
		System.out.println(f);
		int g=~a;
		System.out.println(g);

	}

}
